    /**
     * 将传入的名称的模板的中的枚举类型改为String
     *
     * @param templateName 模板名称
     * @param attributes   要处理的属性名称列表
     */
    public void writeToString(String templateName, List<String> attributes) {
        BufferedReader bufferedReader = null;
        BufferedWriter bufferedWriter = null;
        try {
            String className = DecapitalizeChar.decapitalizeUpperCase(templateName);
            className = getTemplateName(className);
            if (StringUtils.isBlank(className)) {
                logger.error("template 为 {} 的模板没有生成java类名 -------------------------------",className);
                return;
            }

            String classLName = TEMPLATE_CLASSPATH + className;
            String classSName = sourceTemplate + className + lastString;
            String classTName = toTempalte + className + lastString;

            Class<?> aClass = Class.forName(classLName);
            Set<String> fieldNames = new HashSet<>();
            fieldNames = this.getEnumList(aClass, attributes, fieldNames);

            File sFile = new File(classSName);
            File tFile = new File(classTName);
            if (!tFile.exists()) {
                boolean newFile = tFile.createNewFile();
            }
            bufferedReader = new BufferedReader(new InputStreamReader(new FileInputStream(sFile)));
            bufferedWriter = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(tFile)));
            String lineData;
            while ((lineData = bufferedReader.readLine()) != null) {
                // todo 判断 lineData 中是否包含 fieldnames 中的值，有的话替换成String。
                lineData = this.toTheString(fieldNames, lineData);

                bufferedWriter.write(lineData);
                bufferedWriter.newLine();
            }

        } catch (Exception e) {
            logger.error("模板 " + templateName + " 枚举类型改为String异常", e);
        } finally {
            try {
                if (bufferedReader != null)
                    bufferedReader.close();
            } catch (Exception ignored) {

            } finally {
                try {
                    if (bufferedWriter != null)
                        bufferedWriter.close();
                } catch (Exception ignored) {

                }
            }
        }
    }


    /**
     * 获取模板中的枚举值字段
     *
     * @param aClass     模板class对象
     * @param attributes 需要处理的对象
     * @param resultList 返回的list
     * @return 返回值
     */
    public Set<String> getEnumList(Class<?> aClass, List<String> attributes, Set<String> resultList) throws IllegalAccessException, InstantiationException {
        Field[] declaredFields = aClass.getDeclaredFields();
        Class<?> fieldType;
        String name;
        for (Field field : declaredFields) {
            fieldType = field.getType();
            if (fieldType.isEnum()) {
                name = field.getName();
                if (this.contains(attributes, name)) {
                    String name1 = fieldType.getName();
                    String substring = name1.substring(name1.lastIndexOf(".") + 1, name1.length());
                    resultList.add(substring);
                }
            } else if (!ClassReflectionUtil.isBaseType(fieldType) && !ClassReflectionUtil.isListType(field)) {
                this.getEnumList(fieldType, attributes, resultList);
            }
        }
        return resultList;
    }

    private boolean contains(List<String> attributes, String str) {
        if (CollectionUtils.isEmpty(attributes) || StringUtils.isBlank(str))
            return false;
        for (String attr : attributes) {
            if (str.equalsIgnoreCase(attr))
                return true;
        }
        return false;
    }


    /**
     * 将行数据中的枚举值名称改为 String
     *
     * @param fieldNames 枚举值名称
     * @param lineData   行数据
     * @return 字符串
     */
    private String toTheString(Set<String> fieldNames, String lineData) {
        if (CollectionUtils.isEmpty(fieldNames))
            return lineData;
        for (String fieldName : fieldNames) {
            if (lineData.contains(fieldName)) {
                lineData = lineData.replace(fieldName, "String");
            }
        }
        return lineData;
    }


